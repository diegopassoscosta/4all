package br.org.codeforlife.prova2.tipo.animal;

public class Galo extends Animal {
	private int idGalo;
	private int corDaCrista;
	
	public int getIdGalo() {
		return idGalo;
	}
	public void setIdGalo(int idGalo) {
		this.idGalo = idGalo;
	}
	public int getCorDaCrista() {
		return corDaCrista;
	}
	public void setCorDaCrista(int corDaCrista) {
		this.corDaCrista = corDaCrista;
	}
}
