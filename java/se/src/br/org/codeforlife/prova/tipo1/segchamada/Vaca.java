package br.org.codeforlife.prova.tipo1.segchamada;

public class Vaca {
	
	private int peso = 40;
	private String nome = "Vaconilda";
	private int altura = 10;
	
	public int retornaPeso() {
		return peso;
	}
	public void ajustaPeso(int peso) {
		this.peso = peso;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}
	
	
	
	
}
