package br.org.codeforlife.prova.tipo2;

public class Vaca{
	//Atributo que guarda a característica da Vaca.
	private int idade;
	
	/**
	 * Método (ação) que ajusta a idade da vaca
	 * @param novaIdade
	 */
	public void ajustaIdade(int novaIdade){
		idade = novaIdade;
	}
	
	/**
	 * Método (ação) que retorna a idade da Vaca
	 * @return
	 */
	public int retornaIdade(){
		return idade;
	}
	
}


